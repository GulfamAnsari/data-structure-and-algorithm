//////////************ 1. Sliding Window ***************///////////
// -> In this type of pattern, you have to take a small window for your output and decrease that window.
// OR
// -> First add inputs in your window to get the desired output and remove the inputs from window when the
// condition becomes false
// -> All problems titled like (Find longest string, find smallest, find minimum subarray, etc) will come under sliding pattern.
// Example: Minimum Size Subarray Sum
// Example: Longest Substring Without Repeating Characters
// Data Structure Involved: Array, String, and hash table


//////////************ 2. Two pointres ***************///////////
// -> This technique is used two pointer to iterate input dara.
// Generally, these pointers moves in opposite direction.
// Example: Sum of two number equals to target.
// Example: Squares of a Sorted Array
// Data Structure Involved: Array, linklist


//////////************ 3. Cyclic sort ***************///////////
// -> This technique is used when you have given numbers in a fixed range.
// Data Structure Involved: Array
// Example: Find the missing number


//////////************ 4. Merge Intervals ***************///////////
// -> This technique is deal with the overlaping intervals
// Data Structure Involved: Array, heap
// Example: Merge Intervals
// Example: Insert Intervals
// Example: Minimum Meeting rooms
// Example: Conflicting appoinments

//////////************ 5. Slow and fast Pinter ***************///////////
// -> Technique in which we have to take 2 pointer where both the pointers
// traverse the input at different speed.
// Data Structure Involved: Array, String, LinkList
// Example: Middle of lisk list
// Example: Cycle in link list
// Example: Happy number

//////////************ 6. Inplace reversal of linklist ***************///////////
// -> Technique in which we have to exchange the links between the nodes
// traverse the input at different speed.
// Data Structure Involved: LinkList
// Example: Reverse a link list
// Example: rotate a link list
// Example: Reverse every k-elements sub-list


//////////************ 7. Modifies Binary search ***************///////////
// -> Used this technique in sorted array to find an element. It is basically the updated version of binary search.
// traverse the input at different speed.
// Data Structure Involved: Array
// Example: Ceiling of a number
// Example: Next Letter
// Example: Number Range

//////////************ 8. Breath first search(BFS) ***************///////////
// -> Used to solve the problem by iterating the graph or tree in BFS manner.
// Data Structure Involved: Tree, Graph, and Matrix
// Example: Level order traversal
// Example: Depth of a binary tree


//////////************ 9. Depth first search(DFS) ***************///////////
// -> Used to solve the problem by iterating the graph or tree in DFS manner.
// Data Structure Involved: Tree, Graph, and Matrix
// Example: Binary tree path sum

//////////************ 10. Matrix Traversal ***************///////////
// -> Used to solve the problem by traversing matrix in DFS or BFS way.
// Data Structure Involved: Graph, and Matrix
// Example: No of islands


